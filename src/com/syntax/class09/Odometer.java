package com.syntax.class09;

public class Odometer {
	public static void main(String[] args) {
		// print the car odometer 999.9
		for (int a = 0; a < 10; a++) {
			for (int b = 0; b < 10; b++) {
				for (int c = 0; c < 10; c++) {
					for (int d = 0; d < 10; d++) {
						System.out.println(a + " " + b + " " + c + " " + d);
					}
				}
			}
		}
	}
}
