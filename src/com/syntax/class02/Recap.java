package com.syntax.class02;

public class Recap {
	
//	First we create a Project. 
//	Inside our project we have a Source Code.
//	Inside of project we create Packages and Classes.
//	Inside our Source Code we have Packages to organize our code.
//	Inside our Packages we have Classes.
//	Inside our Class we have Main Method - it is our execution point,
//	the entire code that we will be execute should be in Main Method.
	
//	Project: 
//	JRE - java runtime environment, it is our library, Java 8 version
//	src: - source
//	    packages: (name reverse of the url)
//	             classes:
//	                     main method
	
//	In Java we have Keywords and Identifiers.
	
//	Keywords - some reserve words, they have special meaning for our Java.
//	Long, break, if, public, byte..
	
//	Identifiers - it is a simple names we give to classes, methods, variables.
//	Rules: no space, cannot start with number, do not use keywords, no special characters (except $ or _)
//	Conventions: follow CamelCasing, classes should start with upper case,
//	variables and methods start with lower case.
	
//	Data types in Java - 2: primitive and non-primitive.
	
//	Primitive - 8 - byte, short, int, long, 
//	                float, double,
//	                char (for non-numeric), boolean (true, false)
//	Non-primitive - 1 - String
	
//	We store values into variables:
//	Rule to create a variable: 
//	1. data type
//	2. name it
//	3. value (that will match the data type)
}
